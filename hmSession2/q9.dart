// Switch Case with Seasons
// Objective: Use switch for multiple cases.
// Instructions:
// - Create an integer variable month with a value between 1 and 12.
// - Use a switch statement to print the season based on the month.

void main() {
  int month = 13;
  switch (month) {
    case 1:
      print("the season is winter");
      break;
    case 2:
      print("the season is winter");
      break;
    case 3:
      print("the season is spring");
      break;
    case 4:
      print("the season is spring");
      break;
    case 5:
      print("the season is spring");
      break;
    case 6:
      print("the season is summer");
      break;
    case 7:
      print("the season is summer");
      break;
    case 8:
      print("the season is summer");
      break;
    case 9:
      print("the season is fall");
      break;
    case 10:
      print("the season is fall");
      break;
    case 11:
      print("the season is fall");
      break;
    case 12:
      print("the season is winter");
      break;
    default:
      print("the number is not a month number....");
  }
}
